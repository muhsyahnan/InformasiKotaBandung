      <b>
        <h3>Sejarah Masjid Raya Bandung</h3>
      </b><br>
      Sejumlah ahli sejarah menyatakan Masjid Raya Bandung didirikan pada 1812 dengan bentuk bangunan panggung tradisional yang sederhana, bertiang kayu, berdinding anyaman bambu, beratap rumbia, dan dilengkapi sebuah kolam besar sebagai tempat mengambil wudhu.
      Sumber lain menyatakan bahwa Masjid Raya Bandung dibangun bersamaan dengan pembangunan Pendopo Kabupaten Bandung di selatan alun-alun yang diresmikan pada 25 September 1810. Sebuah pendapat berdasar mengingat masjid raya, alun- alun, dan pendopo kabupaten merupakan salah satu elemen pusat kota tradisional di masa Hindia Belanda, sebagai simbol religiusitas pemerintahan dan masyarakat serta sebagai pusat keagamaan kota. Pada tahun 1826, Masjid Raya secara berangsur-angsur menjadi bangunan berkonstruksi kayu. Lalu, pada 1850 beberapa bangunan di alun- alun juga dirombak untuk meningkatkan kualitas fisiknya. Bangunan Masjid Raya diganti dengan tembok batu bata dan atap genting atas prakarsa Bupati R.A. Wiranatakoesoemah IV atau Dalem Bintang (1846-1874). Pada masa ini Masjid Raya sudah dilengkapi pagar tembok setinggi dua meter bermotif sisik ikan yang merupakan gaya ornamen khas Priangan. Lalu, pada tahun 1900 Masjid Raya dibuat lebih representatif, lengkap dengan ciri khusus seperti masjid tradisional pada umumnya. Masjid dibuat berbentuk segi empat dan beratap tumpang susun tiga. Tempat ibadah tersebut sudah dilengkapi mihrab, pawestren (ruang shalat untuk wanita), bedug, kentongan, dan kolam, tetapi belum memiliki menara. Baru pada tahun 1930, berdasarkan rancangan arsitek Maclaine Pont, Masjid Raya dilengkapi serambi atau pendopo depan dan sepasang menara pendek beratap tumpang susun di kiri dan kanan bangunan. Pada tahun 1955, Masjid Raya kembali dirombak total. Perubahan drastis tampak pada bagian atap. Atap tumpang susun tiga yang dipakai sejak 1850 diubah menjadi kubah model bawang bergaya Timur Tengah. Selain itu, kedua menara pendek juga dibongkar, serambi diperluas, dan ruang panjang di kiri-kanan masjid dijadikan satu dengan bangunan induk. Sebuah menara tunggal didirikan di halaman depan masjid bagian selatan. Setelah mengalami beberapa kali perbaikan akibat kerusakan, baik karena faktor alam maupun usia, akhirnya bentuk masjid menjadi seperti yang terlihat saat ini. Dua menara kembar yang menjulang setinggi 81 meter menjadi ciri khas utama masjid. Konon, menurut rencana menara tersebut akan dibuat setinggi 99 meter sesuai jumlah nama-nama Allah dalam Asmaâ€™ul Husna. Namun, karena alasan keamanan lalu lintas udara, ketinggian yang diizinkan hanya 81 meter. Akan tetapi, menurut Ir. Gilang Nugroho selaku site manager, ketinggian menara kembar ini tetap 99 meter jika dihitung dari pondasi setinggi 18 meter.
