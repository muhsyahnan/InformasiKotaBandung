      <p class="text-justify">
        <br>Masjid Raya Bandung merupakan kebanggaan warga Bandung dan sekitarnya. Selain bersejarah dan megah, masjid ini telah menjadi landmark dan penyeimbang segala aspek kehidupan bermasyarakat bagi warga ibukota Jawa Barat tersebut. Bangunan yang tampak saat ini merupakan hasil pembangunan kembali pada awal 2006 lalu. Renovasinya termasuk penataan ulang alun-alun Kota Bandung dan taman kota. Sepanjang sejarahnya, masjid ini telah berkali-kali diubah. Pada abad ke-19 saja dilakukan tiga kali perombakan, kemudian lima kali pada abad ke-20, dan terakhir satu kali renovasi besar- besaran di abad ke-21.
