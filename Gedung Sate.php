      <b>
        <h3>Sejarah Gedung Sate</h3>
      </b><br>
      Gedung Sate ini memiliki sejarah panjang yang sudah ada sejak masa Kolonial BelAnda. Peletakan batu pertamanya dilakukan pada 27 Juli 1920 yang dihadiri oleh Johanna Catherina Coops yang merupakan putri tertua Wali Kota Bandung dan B. Coops serta Petronella Roelofsen yang mewakili Gubernur Jenderal di Batavia. Perancang gedung bersejarah ini dilakukan oleh beberapa arsitek Ir. J. Gerber dari Fakultas Teknik Delft Nederland, Ir. Eh. De Roo, dan Ir. G. Hendriks, dan dari Gemeente vna Bandoeng. Selain menonjolkan unsur tradisional nusantara, Gedung Sate ini juga mengadopsi beberapa aliran arsitek pada beberapa bagian gedung ini. Desain jendela mengusung konsep Moor Spanyol. Gedung ini menggunakan model Rennaisance Italia, model atap mengadopsi unsur Asia seperti Pura di Bali dan Pagoda di Thailand. Ornamen batu yang ada di gedung ini juga sering disebut0sebut sebagai adopsi dari arsitektur Candi Brorobudur. Akulturasi itulah yang membuat Gedung Sate Bandung ini menjadi sangat indah dan kaya akan nilai budaya.
      Pembuatan gedung ini dilakukan oleh 2.000 pekerja dan 150 orang pemahat yang berasal dari berbagai kawasan di sekitar Bandung. Pembangunan berlangsung selama 4 tahun dan selesai pada September 1924. Awalnya gedung ini digunakan untuk aktivitas Departemen Lalu Lintas dan Pekerjaan umum hingga pernah menjadi pusat Pemerintahan Hindia Belanda di Bandung. Pada 3 Desember terjadi peristiwa bersejarah di gedung ini yaitu adanya korban yang jatuh sebanyak 7 orang karena ingin mempertahankan Gedung Sate ini dari pasukan Gurkha. Untuk mengenang 7 pemuda tersebut, maka dibuatlah tugu batu yang ada di halaman belakang Gedung Sate. Kemudian pada 3 Desember 1970 atas perintah dari Menteri Pekerjaan Umum, tugu tersebut akhirnya dipindah ke halaman depan gedung tersebut. Sejak 1980, Gedung Sate Bandung ini disebut juga sebagai Kantor Gubernur karena menjadi pusat pemerintahan Provinsi Jawa Barat. Pada tahun 1977, dibangun gedung baru yang masih bergaya ala Gedung Sate yang digunakan khusus bagi Pimpinan dan Anggota DPRD Provinsi Jawa Barat untuk bertugas dan menjalankan fungisnya. Hingga saat ini Gedung Sate tak pernah sepi pengunjung, baik wisatawan dari dalam atau luar negeri pasti menyempatkan diri datang ke gedung ini saat berada di Bandung.
