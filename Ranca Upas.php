      <h3 class="text-center fw-bold">Ranca Upas</h3>
      <br>Ranca Upas atau Kampung Cai Ranca Upas adalah salah satu bumi perkemahan di Bandung, Jawa Barat, Indonesia. Terletak di Jalan Raya Ciwidey Patenggang KM. 11, Alam Endah, Ciwidey Kabupaten Bandung, dengan jarak sekitar 50 km dari pusat Kota Bandung. Memiliki luas area sekitar 215 Hektar, berada pada 1700 meter di atas permukaan laut, dengan suhu udara sekitar 17 °C - 20 °C. Sekitar area, oleh hutan lindung dengan beragam flora seperti Pohon Huru, Hamirug, Jamuju, Kihujan, Kitambang, Kurai, Pasang dan Puspa. Sedangkan fauna terdiri dari beragam jenis burung, serta beberapa satwa jinak lainnya. </p></br>

      <b>
        <h3> Sejarah Ranca Upas</h3>
      </b><br>
          Ranca Upas Ciwidey menjadi salah satu tempat pelatihan Kopassus, dulunya ranca upas merupakan hutan belantara dengan rawa yang luas. Setelah hutan tersebut bebas dari hewan buas, Ranca Upas dijadikan hutan lindung oleh pihak Perhutani dan kemudian membuka lahan tersebut untuk menjadi sebuah Camping Ground. Ranca Upas Ciwidey, diambil dari kata bahasa Sunda yaitu "Ranca" yang berarti "Rawa", dan "Upas" adalah seorang petugas Perhutani yang melegenda dikawasan Gunung Patuha. Diceritakan bahwa Upas merupakan seorang pria yang berbadan kekar dengan tinggi 198 cm, dia berkebangsaan Belanda. Dia meninggal saat melaksanakan tugas lapangan untuk menjelajahi rawa di kawasan yang sekarang menjadi Ranca Upas, yang sampai saat ini mayatnya tidak pernah ditemukan. Oleh masyarakat setempat dipercaya bahwa arwah Upas, masih ada di alam dunia ini. Pada tahun 1960-1980, masih banyak masyarakat setempat yang bertemu dengan Upas saat mencari kayu bakar di hutan. Konon dikatakan bahwa Upas selalu memakai topi laken (koboi) dengan diameter yang lebar lengkap dengan seragam Perhutani yang pada masa itu berwarna coklat dengan kepala menunduk ditutupi topi lebarnya. Pernah dikatakan seseorang menemukan sebuah batu dengan bentuk hampir menyerupai macan yang sedang duduk. Namun sampai saat ini, batu tersebut masih menjadi mitos masyarakat setempat. Menurut sejarah sebenarnya, asal mula kata "Upas" sebenarnya adalah berasal dari jenis pohon beracun dari keluarga moraceae yang dinamakan Upas. Pada saat dibangun sebuah penangkaran rusa di Ranca Upas oleh pihak Perhutani, hanya seorang juru kunci yang dapat melakukan pemanggilan terhadap rusa-rusa tersebut dan memberikan makanan secara langsung, dia adalah Abah Taji. Namun sekarang setiap orang dapat berinteraksi dan menikmati keindahan dari makhluk yang bertanduk indah ini.</p></br>

