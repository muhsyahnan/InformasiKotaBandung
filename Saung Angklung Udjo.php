      <b>
        <h3>Sejarah Angklung Udjo</h3>
      </b><br>
      Didirikan pada tahun 1966 oleh Udjo Ngalagena dan istrinya Uum Sumiati, dengan maksud untuk melestarikan dan memelihara seni dan kebudayaan tradisional Sunda. Berlokasi di Jalan Padasuka 118, Bandung Timur Jawa Barat Indonesia. Udjo Ngalegana, seorang pengrajin tradisional, membangun tempat itu pada tahun 1966 sebagai sarana untuk memberi kembali kepada masyarakat. Semua hasil Saung Angklung Udjo digunakan untuk pendidikan siswa lokal, yang sebagian besar adalah anak-anak kurang mampu di daerah tersebut. Dengan suasana tempat yang segar udaranya dan dikelilingi oleh pohon-pohon bambu, dari kerajinan bambu dan interior bambu sampai alat musik bambu. Disamping pertunjukan rutin setiap sore, Saung Angklung Udjo telah berkali-kali mengadakan pertunjukan khusus yang dilakukan pada pagi atau siang hari. Pertunjukkan tersebut tidak terbatas diadakan di lokasi Saung Angklung Udjo saja, tetapi berbagai undangan tampil di berbagai tempat baik di dalam maupun di luar negeri, pada bulan Agustus tahun 2000 di Sasana Budaya Ganesha ITB, Bandung, Saung Angklung Udjo mengadakan konser kolaborasi dengan penyanyi cilik yang dijuluki Shirley Temple-nya Indonesia, yaitu Sherina. Saung Angklung Udjo tidak terbatas pada hanya menjual seni pertunjukan saja, berbagai produk alat musik bambu tradisional (angklung, arumba, calung dan lainnya) dibuat dan dijual kepada para pembeli.
      </p> <br>
